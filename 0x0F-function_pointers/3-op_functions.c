/**
 * op_add - add two number
 * @a: the first number
 * @b: the second number
 *
 * Return: sum of the numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two number
 * @a: the first number
 * @b: the second number
 *
 * Return: difference of the numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - muliplies two number
 * @a: the first number
 * @b: the second number
 *
 * Return: multiple of the numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two number
 * @a: the first number
 * @b: the second number
 *
 * Return: division of the numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the modulo of the two number
 * @a: the first number
 * @b: the second number
 *
 * Return: first number modulo the second
 */
int op_mod(int a, int b)
{
	return (a % b);
}
